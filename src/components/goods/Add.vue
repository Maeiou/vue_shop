<template>
  <div>
    <el-breadcrumb separator-class="el-icon-arrow-right">
      <el-breadcrumb-item :to="{ path: '/home' }">首页</el-breadcrumb-item>
      <el-breadcrumb-item>商品管理</el-breadcrumb-item>
      <el-breadcrumb-item :to="{ path: '/goods' }">商品列表</el-breadcrumb-item>
      <el-breadcrumb-item>添加商品</el-breadcrumb-item>
    </el-breadcrumb>
    <el-card>
      <el-alert
        title="添加商品信息"
        type="info"
        center
        show-icon
        :closable="false"
      >
      </el-alert>
      <el-steps :active="activeIndex * 1" finish-status="success" align-center>
        <el-step title="基本信息"></el-step>
        <el-step title="商品参数"></el-step>
        <el-step title="商品属性"></el-step>
        <el-step title="商品图片"></el-step>
        <el-step title="商品内容"></el-step>
        <el-step title="完成"></el-step>
      </el-steps>
      <!-- tab栏区域 -->
      <el-form
        :model="addForm"
        :rules="addFormRules"
        ref="addFormRef"
        label-width="100px"
        label-position="top"
      >
        <el-tabs
          :tab-position="'left'"
          v-model="activeIndex"
          :before-leave="beforeTabsLeave"
        >
          <el-tab-pane label="基本信息" name="0">
            <el-form-item label="商品名称" prop="goods_name">
              <el-input v-model="addForm.goods_name"></el-input>
            </el-form-item>
            <el-form-item label="商品价格" prop="goods_price">
              <el-input v-model="addForm.goods_price"></el-input>
            </el-form-item>
            <el-form-item label="商品数量" prop="goods_number">
              <el-input v-model="addForm.goods_number"></el-input>
            </el-form-item>
            <el-form-item label="商品重量" prop="goods_weight">
              <el-input v-model="addForm.goods_weight"></el-input>
            </el-form-item>
            <el-form-item label="商品分类" prop="goods_cat">
              <el-cascader
                v-model="addForm.goods_cat"
                :options="catelist"
                :props="cateProps"
                @change="handleChanged"
                clearable
              ></el-cascader>
            </el-form-item>
          </el-tab-pane>
          <el-tab-pane label="商品参数" name="1">
            <!--  表单Item项-->
            <el-form-item
              :label="item.attr_name"
              v-for="item in manyTableData"
              :key="item.attr_id"
              ><el-checkbox-group v-model="item.attr_vals">
                <el-checkbox
                  :label="cb"
                  v-for="(cb, i) in item.attr_vals"
                  :key="i"
                  border
                >
                </el-checkbox> </el-checkbox-group></el-form-item
          ></el-tab-pane>
          <el-tab-pane label="商品属性" name="2"
            ><el-form-item
              :label="item.attr_name"
              v-for="item in onlyTableData"
              :key="item.attr_id"
              ><el-input v-model="item.attr_vals"></el-input></el-form-item
          ></el-tab-pane>
          <el-tab-pane label="商品图片" name="3"
            ><!-- action 图片上传的后台api地址 --><el-upload
              :action="uploadUrl"
              :on-preview="handlePreview"
              :on-remove="handleRemove"
              list-type="picture"
              :headers="headerObj"
              :on-success="handleSuccess"
            >
              <el-button size="small" type="primary">点击上传</el-button>
              <div slot="tip" class="el-upload__tip">
                只能上传jpg/png文件，且不超过500kb
              </div>
            </el-upload></el-tab-pane
          >
          <el-tab-pane label="商品内容" name="4">
            <!-- 富文本编辑器 -->
            <quill-editor v-model="addForm.goods_introduce" />
          </el-tab-pane>
          <el-button @click="lastStep" :disabled="activeIndex * 1 === 0"
            >上一步</el-button
          >
          <el-button @click="nextStep">下一步</el-button>
          <el-button type="primary" @click="add">添加商品</el-button>
        </el-tabs>
      </el-form>
    </el-card>
    <!-- 图片预览 -->
    <el-dialog title="提示" :visible.sync="previewVisible" width="50%">
      <img :src="previewPath" alt="" class="previewImg" />
    </el-dialog>
  </div>
</template>

<script>
export default {
  data() {
    return {
      activeIndex: '0',
      addForm: {
        goods_name: '',
        goods_price: 0,
        goods_number: 0,
        goods_weight: 0,
        goods_cat: [],
        /* 图片所属的分类数组 */
        pics: [],
        /* 商品详情描述 */
        goods_introduce: '',
        attrs: [],
      },
      addFormRules: {
        goods_name: [
          { required: true, message: '请输入商品名称', trigger: 'blur' },
        ],
        goods_price: [
          { required: true, message: '请输入商品价格', trigger: 'blur' },
        ],
        goods_number: [
          { required: true, message: '请输入商品数量', trigger: 'blur' },
        ],
        goods_weight: [
          { required: true, message: '请输入商品重量', trigger: 'blur' },
        ],
        goods_cat: [
          { required: true, message: '请选择商品分类', trigger: 'blur' },
        ],
      },
      catelist: [],
      cateProps: {
        value: 'cat_id',
        label: 'cat_name',
        children: 'children',
        expandTrigger: 'hover',
      },
      manyTableData: [],
      onlyTableData: [],
      /* 图片上传的请求头对象 */
      headerObj: {
        Authorization: window.sessionStorage.getItem('token'),
      },
      uploadUrl: 'http://www.ysqorz.top:8888/api/private/v1/upload',
      previewPath: '',
      previewVisible: false,
    }
  },
  created() {
    this.getCateList()
  },
  methods: {
    async getCateList() {
      const { data: res } = await this.$http.get('categories')
      this.catelist = res.data
    },
    handleChanged() {
      console.log(this.addForm.goods_cat)
      if (this.addForm.goods_cat.length !== 3) {
        this.addForm.goods_cat = []
      }
    },
    beforeTabsLeave(activeName, oldActiveName) {
      if (this.addForm.goods_name.trim() === '') {
        this.activeIndex = oldActiveName
        this.$message.error('请输入商品名称')

        return false
      }
      if (this.addForm.goods_cat.length !== 3) {
        this.$message.error('请先选择商品分类')
        this.activeIndex = oldActiveName
        return false
      }
      switch (activeName) {
        case '1':
          this.getManyParams()
          break
        case '2':
          this.getOnlyParams()
          break
      }
    },
    /* 上一步 */
    lastStep() {
      this.activeIndex = this.activeIndex * 1 - 1 + ''
    },
    /* 下一步 */
    nextStep() {
      this.activeIndex = this.activeIndex * 1 + 1 + ''
    },

    async getManyParams() {
      const { data: res } = await this.$http.get(
        `categories/${this.cateId}/attributes`,
        {
          params: { sel: 'many' },
        }
      )
      res.data.forEach((item) => {
        item.attr_vals =
          item.attr_vals.length === 0 ? [] : item.attr_vals.split(',')
      })
      this.manyTableData = res.data

      console.log(this.manyTableData)
    },
    async getOnlyParams() {
      const { data: res } = await this.$http.get(
        `categories/${this.cateId}/attributes`,
        {
          params: { sel: 'only' },
        }
      )

      this.onlyTableData = res.data
    },
    /* 处理图片预览效果 */
    handlePreview(file) {
      console.log(file)
      this.previewPath = file.url
      this.previewVisible = true
    },
    /* 移除图片 */
    handleRemove(file) {
      /* 要移除图片的临时路径 */
      console.log(file)
      const filePath = file.response.data.tmp_path
      /* 从pics数组中找到这个图片的索引值 */
      const i = this.addForm.pics.findIndex((x) => x.pic === filePath)
      console.log(i)
      /* 调用数组的splice 方法，把图片信息对象，从pics数组中移除 */
      this.addForm.pics.splice(i, 1)
      console.log(this.addForm)
    },
    /* 图片上传成功的事件 */
    handleSuccess(response) {
      /* 拼接得到一个图片信息对象 */
      const picsInfo = {
        pic: response.data.tmp_path,
      }
      /* 将图片信息对象，push到pics数组中 */
      this.addForm.pics.push(picsInfo)
      console.log(this.addForm)
    },
    /* 添加商品 */
    add() {
      console.log(this.addForm)
      this.$refs.addFormRef.validate(async (valid) => {
        if (!valid) {
          return this.$message.error('请填写必要的表单项')
        }

        /* 深度拷贝 lodash*/
        /* const form = lodash.cloneDeep(this.addForm)
      form.goods_cat = form.goods_cat.join(',')
      console.log(form) */
        var form = JSON.parse(JSON.stringify(this.addForm))

        form.goods_cat = form.goods_cat.join(',')
        this.manyTableData.forEach((item) => {
          const newInfo = {
            attr_id: item.attr_id,
            attr_value: item.attr_vals,
          }
          form.attrs.push(newInfo)
        })
        this.onlyTableData.forEach((item) => {
          const newInfo = {
            attr_id: item.attr_id,
            attr_value: item.attr_vals,
          }
          form.attrs.push(newInfo)
        })

        console.log(form)
        const { data: res } = await this.$http.post('goods', form)
        if (res.meta.status !== 201) {
          return this.$message.error('添加商品失败')
        }
        this.$message.success('添加商品成功')
        this.$router.push('/goods')
      })
    },
  },
  computed: {
    cateId() {
      if (this.addForm.goods_cat.length === 3) {
        return this.addForm.goods_cat[2]
      }
      return null
    },
  },
}
</script>

